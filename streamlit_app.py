import streamlit as st

st.title("🖥️ 고등학생을 위한 파이썬 자습실 (조건문 마스터 버전)")

# -------------------------------------------------------------
# [계획 B 반영] 중첩 조건문을 활용한 "나의 학습 코스 추천"
# -------------------------------------------------------------
st.header("1. 나에게 맞는 파이썬 공부 코스 추천 🧭")

# 1차 조건 입력: 실력
level = st.selectbox("현재 나의 코딩 실력은?", ["선택하세요", "초보 (print문 겨우 씀)", "중수 (반복문 이해함)"])
# 2차 조건 입력: 시간
study_time = st.number_input("오늘 공부할 수 있는 시간(분단위):", min_value=0, max_value=180, value=30)

if st.button("추천 코스 보기"):
    # 유효성 검사 (아무것도 선택 안 했을 때) - 계획 C의 원리
    if level == "선택하세요":
        st.warning("⚠️ 먼저 실력을 선택해 주세요!")
    
    # 본격적인 중첩 조건문 시작
    elif level == "초보 (print문 겨우 씀)":
        if study_time < 30: # 초보이면서 30분 미만 공부
            st.success("🏃 코스: '기초 문법 10분 컷 영상 보기'를 추천합니다.")
        else: # 초보이면서 30분 이상 공부
            st.success("📖 코스: '파이썬 변수와 데이터 타입 교과서 읽기'를 추천합니다.")
            
    elif level == "중수 (반복문 이해함)":
        if study_time < 30: # 중수이면서 30분 미만 공부
            st.success("💻 코스: '백준 퀴즈 1문제 풀기'를 추천합니다.")
        else: # 중수이면서 30분 이상 공부
            st.success("🚀 코스: '스트림릿으로 나만의 웹 앱 만들기 실습'을 추천합니다.")

st.markdown("---")

# -------------------------------------------------------------
# [계획 A 반영] 다중 조건문을 활용한 "최종 결과 등급 매기기"
# -------------------------------------------------------------
st.header("2. 나의 파이썬 모의고사 점수 계산기 💯")
st.write("오늘 본 수행평가나 퀴즈 점수를 입력하면 등급을 계산해 줍니다.")

# 점수 입력 받기
score = st.slider("점수를 선택하세요 (0점 ~ 100점):", 0, 100, 70)

if st.button("등급 확인"):
    # 연속적인 범위를 판별하는 if - elif - else 구조
    if score >= 90:
        st.balloons() # 축하 효과
        st.success(f"🏅 점수: {score}점 | 등급: **[A] 코딩 대왕**")
        st.write("이미 고등학교 수준을 넘어섰군요! 친구들을 도와주세요.")
    elif score >= 70:
        st.info(f"🥈 점수: {score}점 | 등급: **[B] 열정 가득 개발자**")
        st.write("조금만 더 하면 전교 상위권입니다. 화이팅!")
    elif score >= 50:
        st.warning(f"🥉 점수: {score}점 | 등급: **[C] 자라나는 파이썬 새싹**")
        st.write("개념을 조금 더 복습하면 금방 실력이 늘 거예요.")
    else:
        st.error(f"🌱 점수: {score}점 | 등급: **[D] 코드 헬퍼가 필요한 상태**")
        st.write("괜찮아요! 이 앱의 '에러 사전' 탭에서 처음부터 다시 공부해봐요.")